package Main;

public class Client {
	
	String Name;
	String Email;
	String Number;

	public Client(String Name, String Number, String Email) {
		Name = this.Name;
		Email = this.Email;
		Number = this.Number;
		
	}
	public String getName() {
		return Name;
	}
	
	public String getEmail() {
		return Email;
	}
	
	public String getNumber() {
		return Number;
	}
}
